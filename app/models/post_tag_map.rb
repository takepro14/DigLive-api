class PostTagMap < ApplicationRecord

  ####################################################################################################
  # アソシエーション
  ####################################################################################################
  belongs_to :post
  belongs_to :tag

  ####################################################################################################
  # バリデーション
  ####################################################################################################
  validates :post_id, presence: true
  validates :tag_id, presence: true
  validates :post_id, uniqueness: { scope: :tag_id }

end
